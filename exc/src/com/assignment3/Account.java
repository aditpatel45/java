package com.assignment3;

public class Account {
	private String acctype;
	private double accnum;
	private int minbal;
	private double currbal;
	public String getAcctype() {
		return acctype;
	}
	public void setAcctype(String acctype) {
		this.acctype = acctype;
	}
	public double getAccnum() {
		return accnum;
	}
	public void setAccnum(double accnum) {
		this.accnum = accnum;
	}
	public int getMinbal() {
		return minbal;
	}
	public void setMinbal(int minbal) {
		this.minbal = minbal;
	}
	public double getCurrbal() {
		return currbal;
	}
	public void setCurrbal(double currbal) {
		this.currbal = currbal;
	}
	
}
